public class CreditMatchChangeData
{
    /**
     * 抵押包数据
     */
    private WmsInveCreditPackage creditPackage;

    /**
     * 单据债权关联数据
     */
    private WmsInveTransaCrepkg transaCrepkg;

    /**
     * 匹配的金额
     */
    private BigDecimal productAccount;

    /**
     * 债权到期时间
     */
    private Date creditEndDate;

    /**
     * @return the transaCrepkg
     */
    public WmsInveTransaCrepkg getTransaCrepkg()
    {
        return transaCrepkg;
    }

    /**
     * @param transaCrepkg the transaCrepkg to set
     */
    public void setTransaCrepkg(WmsInveTransaCrepkg transaCrepkg)
    {
        this.transaCrepkg = transaCrepkg;
    }

    /**
     * @return the productAccount
     */
    public BigDecimal getProductAccount()
    {
        return productAccount;
    }

    /**
     * @param productAccount the productAccount to set
     */
    public void setProductAccount(BigDecimal productAccount)
    {
        this.productAccount = productAccount;
    }

    /**
     * @return the creditPackage
     */
    public WmsInveCreditPackage getCreditPackage()
    {
        return creditPackage;
    }

    /**
     * @param creditPackage the creditPackage to set
     */
    public void setCreditPackage(WmsInveCreditPackage creditPackage)
    {
        this.creditPackage = creditPackage;
    }

    /**
     * @return the creditEndDate
     */
    public Date getCreditEndDate()
    {
        return creditEndDate;
    }

    /**
     * @param creditEndDate the creditEndDate to set
     */
    public void setCreditEndDate(Date creditEndDate)
    {
        this.creditEndDate = creditEndDate;
    }

}
